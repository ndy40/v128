                    <p>
                        In the early years of the 19th century, the Temne, under King Tom, made frequent attacks on Freetown. Fort Thornton was not yet 
                        completed, and Governor Day in 1805, caused to be erected for the defence of Freetown, a Martello Tower which was a replica of the 
                        towers at that time being put up all along the South coast of England to counter the threatened invasion by Napoleon I. Governor 
                        Day got the latest model from the Board of Ordinance and on April 11th, 1805, he led a procession up Wansey Hill, renamed “Tower 
                        Hill”, to lay the foundation stone. It is believed that no shot was ever fired in anger from the tower.
                    </p>

                    <p>
                        Round about 1870, piped water was first introduced in Freetown. The initial supply came from the upper waters of Nicol Brook. The 
                        pipe was laid to Tower Hill Barracks, and then down to the old military hospital to Government House and on into the town. About 
                        this time the tower was truncated and a large iron water tank placed on the top, controlling the water supply to Government House.
                    </p>

                    <p>[The tower was proclaimed a national monument to save it from demolition when the new Parliament Building was being built in 1961.]</p>
