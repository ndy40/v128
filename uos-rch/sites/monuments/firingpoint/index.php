                    <p>
                        Because many pirates were operating in international waters in the 18th Century, Merchants petitioned the British Government 
                        to help them stamp out piracy. Before the slave trade was abolished in 1807 and before the West African Squadron was 
                        instituted, a Squadron of the British Royal Navy was sent to the West African coast to do away with piracy. Freetown did not 
