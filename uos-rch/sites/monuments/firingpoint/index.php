                        Bunce Island. The other is that of Lieutenant J.W. Roberts, R.N., dated 1847.
                    </p>

                    <p><em><strong>Excerpt from W.W. Shreeve, Sierra Leone: The Principal British Colony on the Western Coast of Africa. London: Simmonds & Co., 1847. (p.20):</strong></em></p>

                    <p>
                        <em>The Island of Bananas</em> is generally the resort of invalids, who shortly become invigorated by the sea air. It contains 
                        a large Government house, the residence of the manager. This island is about four or five miles long, half a mile broad, and 
                        in some places mountainous. It yields abundance of fowls, eggs, yams, &c., and was formerly the property of the Clevelands and 
                        Caulkers, native chiefs; and when the slave trade was legal, the resort of vessels from Liverpool and other places was 
                        considerable. It came into the possession of an English merchant in payment of a debt due by the Caulkers, and was rented by 
                        him to the Government as a residence for liberated Africans; but the manager having been withdrawn, it would appear to be of 
                        little consideration at present.
                    </p>

                    <p>
                        In the burying-ground there is a gravestone upwards of a hundred years old, bearing date 1742, upon which the inscription to 
