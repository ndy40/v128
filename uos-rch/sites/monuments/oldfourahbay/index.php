                    <h3>Notes</h3>

                    <p>
                        <em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and 
                                Relics Commission, n.d. (p.5):</strong></em>
                    </p>

                    <p>
                        This building is situated on Ross Road, Clinetown. It is a massive four-storey structure, built of dressed stone blocks of 
                        laterite. Building operations started in 1845 and the building was completed in 1848. Sierra Leone’s first coloured Governor, 
                        Staff Sergeant Major William Fergusson, laid the foundation stone. It housed the Christian Institution, which in 1876, 
                        developed into the first University College in Black Africa. It was in regular use till the second World War, when the college 
                        was transferred to Mabang in the then Protectorate. Proclaimed a National Monument the building was used as headquarters of 
                        the Sierra Leone Government Railway. At present it is used as a Magistrate Court.
                    </p>

                    <p>
                        <em><strong>Excerpt from A.J.G. Wyse et al. 2002. Vistas of the Heritage of Sierra Leone. Freetown, Sierra Leone: Fourah Bay 
                                College & Sierra Leone National Museum. (p.27-28):</strong></em>
                    </p>

                    <p>
                        Known as the Old Fourah Bay College Building, this four-storied edifice of quaint architecture was constructed by the Revd. 
                        James Beale, a missionary builder, for more than two thousand pounds, the original estimate. The College was built at the same 
                        time as Christ Church, Pademba Road (1844-1847).
                    </p>

                    <p>
                        The building is of the same architectural genre as that represented by Kortright House, the official residence of the 
                        Principal of Fourah Bay College and the S.B. Thomas Agricultural College at Mabang. Although it is still in the books as a 
                        declared National Monument, the Old College building was however in a deplorable state of disrepair in the last decade because 
                        of the rigors of misuse, mismanagement and the devastation caused by the rebel invasion of Freetown on January 6, 1999. 
                        Indeed, it was used as an asylum for internally displaced persons during the rebel war and it was subsequently burnt in 
                        mysterious circumstances after 1999. Now its gutted ruins stand like an accusing finger for all to see. The land on which 
                        this famous building was erected was bought for $330 by the Church Missionary Society from the executors of the Late Governor 
                        Turner’s property at Fourah Bay in the East End of Freetown. On February 5, 1845 when the foundation stone was laid by the 
                        mullato acting governor, William Fergusson, the latter could not recalling that the site used to be a Slave Factory just 40 
                        years previously. As a matter of fact, the roof of the new building was constructed with timber from condemned Slave Ships. 
                        In this dramatic way, this background gave some piquancy to the history of the institution which was to produce in the next 
                        fifty years the first generation of university educated elite in the sub region, many of whom may have been descendants of 
                        these very slaves who were rescued by the British Navy while they were being taken to the new world to work in the 
                        plantations.
                    </p>

                    <p>
                        When completed in 1848, it opened its doors on All Saints Day to admit resident students of the Fourah Bay Institution, 
                        which after this time, was known as Fourah Bay College. The Principal also had his residence in the building. To this 
                        institution came a body of scholars, redoubtable linguistic experts like Sigismund Koelle, the author of Polyglotta Africana 
                        (1854), J.F. Schon, C.A. Reichardt, C.F. Schlenker, and even Adjayi Crowther, whose exertions on African Languages made 
                        Fourah Bay College, “The literary and linguistic workshop of West Africa” in the 1840s and 1850s. Consequently, the competence 
                        and performance of this College established a reputation, which was expressed in the ascription to Freetown as “The Athens of 
                        West Africa” in the second half of the 19th Century.
                    </p>

                    <p>
                        The colour injected into the story by the activities of two of the star individuals connected with the building is no less 
                        significant. The inspiration, drive, foresight and unrelenting advocacy were provided by a son of the diaspora, the Revd. 
                        Edward Jones, the first black graduate from Amherst University, who was appointed Principal of Fourah Bay College in 1841. 
                        His efforts produced such eminent scholars from the College as Africanus Beale Horton and Broughton Davies, the first Sierra 
                        Leonean to qualify as doctors, James “Holy” Johnson, and Archdeacons G.J. Macauley and James Robin, pioneer clerics. James 
                        Johnson was the second African Bishop of the Niger.
                    </p>
