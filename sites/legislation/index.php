            <h2>Heritage Legislation</h2>
            <p>
                <b>No. 12, 1946</b><br />
            </p>
            <p>
                <b>An Ordinance to Provide for the Preservation of Ancient, Historical, 
                and Natural Monuments, Relics and other Objects of Archaeological, 
                Ethnographical, Historical or other Scientific Interest
                </b><br />
            </p>
            <p>
                BE IT ENACTED by the Governor of Sierra Leone, with the advice and 
                consent of the Legislative Council thereof, as follows:<br>
            </p>
            <ol type="1">
                <li>
                    This Ordinance may be cited as the Monuments and Relics Ordinance, 
                    1946, and shall come into operation on such date as the Governor 
                    shall appoint by notice published in the <i>Gazette</i> and shall 
                    apply to the Colony and Protectorate.
                </li>
            </ol>
            <br />
            <ol type="1" start="2">
                <li>In this Ordinance, unless the context otherwise requires:</li>
            </ol>
            <br />
            <ul>
                <p>
                    “ancient monument” means any building, ruin, remaining portion of building 
                    or ruin, stone circle, pillar, statue, tumulus, grave, cave, rock shelter, 
                    kitchen midden, earthwork, iron-smelting site, underground cistern, shell 
                    mound or other site or thing of similar kind, of archaeological, 
                    ethnographical, historical or other scientific interest, but does not 
                    include any ancient working;<br />
                </p>
            </ul>
            <ul>
                <p>
                    “ancient working” means any shaft, cutting tunnel or stope which was made 
                    for mining purposes and which has been proclaimed as such by the Government 
                    under the provisions of section 9;<br />
                </p>
            </ul>
            <ul>
                <p>
                    “ethnographical article” means ceremonial insignia and includes any other 
                    article of archaeological, ethnographical, historical or other scientific 
                    interest which has been proclaimed as such by the Governor under the provisions 
                    of section 9;<br />
                </p>
            </ul>
            <ul>
                <p>“Government” means the Government of Sierra Leone;<br /></p>
            </ul>
            <ul>
                <p>“monument” means –<br></p>
            </ul>
            <ul>
                <ol type="a">
                    <li>any ancient monument;</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="2">
                    <li>any area of land which is of archaeological or historical interest or contains objects of such interest;</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="3">
                    <li>any waterfall, cave, grotto, avenue of trees, old tree or building; and</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="4">
                    <li>any other object (whether natural or constructed by man) of archaeological, ethnographical, historical or other scientific interest;</li>
                </ol>
            </ul>
            <br />
            <ul>
                <p>“national monument” means a monument which has been proclaimed as such by the Governor under the provisions of section 10;<br /></p>
            </ul>
            <ul>
                <p>“relic” means –<br /></p>
            </ul>
            <ul>
                <ol type="a">
                    <li>any fossil of any kind;</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="2">
                    <li>any ancient drawing, engraving or painting on stone or petroglyph;</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="3">
                    <li>any ancient implement, article or ornament; and</li>
                </ol>
            </ul>
            <br />
            <ul>
                <ol type="a" start="4">
                    <li>any archaeological or ethnographical contents of any ancient monument or ancient working.</li>
                </ol>
            </ul>
            <br />
            <ol type="1" start="3">
                <li>
                    (1) As from the commencement of this Ordinance, there shall be established a commission, to be known 
                    as the Monuments and Relics Commission and hereinafter referred to as “the Commission”, which shall 
                    consist of not less than seven members, who shall be appointed by the Governor.
                </li>
            </ol>
            <br />
            <ul>
                <p>
                    (2) If the Commission consists of less than nine members, three shall be quorum. If the Commission 
                    consists of more than nine members not less than one-third of the total membership shall form a quorum.
                    <br />
                </p>
            </ul>
            <ul>
                <p>
                    (3) The members of the Commission shall choose one of their number as Chairman, who shall convene all 
                    meetings of the Commission (other than the first meeting which shall be convened by the Governor). If 
                    at any meeting of the Commission the Chairman is absent, the members present shall choose one of their 
                    number as Chairman at that meeting.
                    <br />
                </p>
            </ul>
            <ul>
                <p>
                    (4) The member presiding as Chairman at any meeting of the Commission shall have a deliberative as well 
                    as a casting vote.
                    <br />
                </p>
            </ul>
            <ul>
                <p>
                    (5) Subject to the provision of subsection (4), a decision of the majority of the members of the 
                    Commission present at any meeting shall be deemed to be a decision of the Commission.
                    <br />
                </p>
            </ul>
            <ol type="1" start="4">
                <li>
                    No remuneration shall be paid to the members of the Commission, but each member may be paid from the 
                    funds of the Commission his reasonable expenses for travelling and subsistence while engaged upon the 
                    business of the Commission at a rate which shall be prescribed by regulation under this Ordinance.
                </li>
            </ol>
            <br />
            <ol type="1" start="5">
                <li>
                    The Commission shall be a body corporate having perpetual succession and a common seal and capable in 
                    law of suing and being sued in its corporate name and, subject to the provisions of this Ordinance, of 
                    acquiring, holding, alienating, hypothecating, letting and hiring property, movable and immovable, and 
                    of performing such acts as bodies corporate may by law perform.
                </li>
            </ol>
            <br />
            <ol type="1" start="6">
                <li>
                    (1) The funds of the Commission shall consist of grants, donations, fees and subscriptions as may from 
                    time to time be made to it or be payable to it.
                </li>
            </ol>
            <br />
            <ul>
                <p>
                    (2) The accounts of the Commission shall be audited annually by such person as the Commission, with the 
                    approval of the Governor, shall appoint.
                    <br />
                </p>
            </ul>
            <ol type="1" start="7">
                <li>(1) The Commission may –</font></li>
            </ol>
            <br />
<ul>
<ol type="a">
  <li>if it has sufficient funds 
  available for the purpose, employ a secretary and such other servants 
  as may be necessary to enable the Commission to carry out its functions 
  and pay their remuneration from its funds.</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>make a list of all monuments 
  whose proclamation as national monuments it considers desirable;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="3">
  <li>take steps to ascertain the 
  owner of any such monument;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="4">
  <li>recommend to the Governor 
  the proclamation of any monument as a national monument;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="5">
  <li>if it has sufficient funds 
  available for the purpose, purchase or otherwise acquire any monument, 
  relic, ethnographical article or other article of archaeological, ethnographical, 
  historical or other scientific interest;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="6">
  <li>assume control over any monument, 
  relic, ethnographical article or other article of archaeological, ethnographical, 
  historical or other scientific interest if requested by the person having 
  the ownership or control thereof so to do, and as trustee for the Government 
  accept any such monument, relic, ethnographical article or other article 
  which the owner desires to give or has bequeathed to the Government;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="7">
  <li>preserve, repair, restore 
  or insure any monument, relic, ethnographical article or other article 
  of archaeological, ethnographical, historical or other scientific interest 
  which is owned by it or under its control;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="8">
  <li>lend or, subject to the provisions 
  of section 8, give any such relic, ethnographical article or other article 
  to any museum or other public institution;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="9">
  <li>undertake the excavation of 
  any ancient monument or ancient working, and by writing under the hand 
  of the chairman authorise any act to be done which it may deem necessary 
  for excavating, exploring or safely preserving any ancient monument 
  or ancient working or any relic wherever situate or found by whomsoever 
  owned or controlled including the placing of appropriate notices;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="10">
  <li>take steps for the erection 
  of notices or tablets in suitable places, giving information about historical 
  events which occurred at such places;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="11">
  <li>through any of its members 
  have reasonable access at all reasonable times to any monument, ancient 
  working, relic, ethnographical article or other article of archaeological, 
  ethnographical, historical or other scientific interest, or believed 
  to be of such interest.</font></li>

</ol>
</ul>
 <br>
<ul><p>(2) The Commission shall – </font> <br>
</p></ul>
<ul>
<ol type="a">
  <li>when required by the Governor, 
  investigate and report upon any matter relating to any monument, ancient 
  working, relic, ethnographical article or other article of archaeological, 
  ethnographical, historical or other scientific interest or believed 
  to be of such interest, and upon the desirability of proclaiming any 
  monument as a national monument;</font></li>
</ol>
</ul>
 <br>

<ul>
<ol type="a" start="2">
  <li>on or before the 31st of March 
  in every year furnish to the Governor a report upon its work during 
  the year which ended on the 31st of December last preceding and on any 
  other matters connected with the preservation of monuments, relics, 
  ethnographical articles or articles of archaeological, ethnographical, 
  historical or other scientific interest which it may desire to bring 
  to the attention of the Governor;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="3">
  <li>make a register of all national 
  monuments and ancient workings and of such relics, ethnographical articles 
  or articles of archaeological, ethnographical, historical or other scientific 
  interest as it may have acquired or as have been brought to its notice.</font></li>
</ol>
</ul>
 <br>

<ol type="1" start="8">
  <li>Save with the written consent 
  of the Governor, the Commission shall not alienate, hypothecate or let 
  any monument, relic, ethnographical article or other article of archaeological, 
  ethnographical, historical or other scientific interest which is owned 
  by it or for which it is trustee for the Government.</font></li>
</ol>
 <br>
<ol type="1" start="9">
  <li>The Governor may from time 
  to time, on the recommendation of the Commission, by notice published 
  in the <i>Gazette</i>, proclaim –</font></li>
</ol>
 <br>
<ul>

<ol type="a">
  <li>any shaft, cutting, tunnel 
  or stope which was made for mining purposes to be an ancient working;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>any article for archaeological, 
  ethnographical, historical or other scientific interest to be an ethnographical 
  article.</font></li>
</ol>
</ul>
 <br>

<ol type="1" start="10">
  <li>The Governor may from time 
  to time, on the recommendation of the Commission, by notice published 
  in the <i>Gazette</i>, proclaim any monument to be a national monument:</font></li>
</ol>
 <br>
<ul><p>Provided that –</font> <br>
</p></ul>
<ol type="i">
  <li>at least one month before 
  recommending to the Governor the proclamation of any monument which 
  does not belong to the Crown, the Commission shall notify the owner 
  thereof in writing that it proposes to make such recommendation, and 
  such owner may thereupon lodge with the Commission objections in writing 
  to the proposed recommendation; and</font></li>

</ol>
 <br>
<ol type="i" start="2">
  <li>in making any such recommendation 
  the Commission shall submit to the Governor proof that the owner concerned 
  has received the said notice and shall also transmit to the Governor 
  any objections which may have been lodged against such recommendation.</font></li>
</ol>
 <br>
<ol type="1" start="11">
  <li>(1) The discovery of any 
  ancient monument, relic or ethnographical article shall be notified 
  in writing to the Commission without delay by the discoverer thereof 
  and also by the owner or occupier of the land upon which such ancient 
  monument, relic or ethnographical article is discovered upon the discovery 
  coming to notice.</font></li>
</ol>
 <br>

<ul><p>(2) Upon receipt of any such notice 
as is mentioned in subsection (1) the Commission shall have a right 
of option to acquire the ownership of the ancient monument, relic or 
ethnographical article so discovered upon payment to the owner of a 
sum of money to be agreed upon as fair and reasonable compensation, 
or failing agreement, upon payment of such a sum as may be determined 
or awarded by such qualified person or persons as the Governor may appoint 
in that behalf.</font> <br></p></ul>
<ul><p>(3) If within six months after the 
receipt of the first notice of any discovery in terms of subsection 
(1) the Commission has failed to notify the owner of such ancient monument, 
relic or ethnographical article that it intends to exercise its option, 
such right of option shall be deemed to have lapsed.</font> <br>
</p></ul>
<ol type="1" start="12">
  <li>No person shall, without 
  the written consent of the Commission, excavate any ancient monument 
  or ancient working or, if he receives the written consent of the Commission, 
  excavate it contrary to any conditions which may have been imposed in 
  such consent or by regulation under this Ordinance.</font></li>
</ol>
 <br>
<ol type="1" start="13">
  <li>(1) No person shall, without 
  the written consent of the Commission –</font></li>

</ol>
 <br>
<ul>
<ol type="a">
  <li>make any alteration to or 
  destroy or damage, or</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>remove from its original site 
  or export from Sierra Leone,</font></li>

</ol>
</ul>
 <br>
<ul><p>any national monument, monument or 
relic or any part thereof.</font> <br></p></ul>
<ul><p>(2) Any person who desires to remove 
from its original site or to export from Sierra Leone any national monument, 
monument or relic or any part thereof shall, when applying to the Commission 

for its consent, supply the Commission with a drawing or photograph 
of the monument or relic or part thereof in question, and shall state 
the exact locality in which it is situated and the place to which and 
the purposes for which he desires to remove or to export it.</font> <br>
</p></ul>
<ul><p>(3) No person shall, without the 
written consent of the Commission, alter, destroy, damage or export 
from Sierra Leone any ethnographical article or any part thereof.</font> <br>
</p></ul>
<ul><p>(4) Any person who desires to export 
from Sierra Leone any ethnographical article or any part thereof shall, 
when applying to the Commission for its consent, submit the article 
of part thereof in question for examination by the Commission or supply 
the Commission with a drawing or photograph of the ethnographical article 
or part thereof in question, and shall state the purposes for which 
he desires to export it.</font> <br></p></ul>

<ol type="1" start="14">
  <li>No person shall deface, damage 
  or destroy any notice or tablet erected by the Commission under the 
  powers conferred by paragraph (i) or (j) of subsection (1) of section 
  7.</font></li>
</ol>
 <br>
<ol type="1" start="15">
  <li>The Governor may make regulations 
  generally for the better carrying into effect of the provisions of this 
  Ordinance, and in particular for prescribing –</font></li>
</ol>
 <br>
<ul>
<ol type="a">
  <li>the procedure in connection 
  with meetings of the Commission;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>the qualification of its members;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="3">
  <li>the period for which its members 
  shall hold office;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="4">
  <li>the rates of travelling and 
  subsistence allowances to be paid to members of the Commission; and</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="5">
  <li>the conditions to be imposed 
  with respect to any ancient monument or ancient working.</font></li>

</ol>
</ul>
 <br>
<ol type="1" start="16">
  <li>(1) The Commission may make 
  bye-laws –</font></li>
</ol>
 <br>
<ul>
<ol type="a">
  <li>regulating the access of the 
  public to any monuments, relics, ethnographical articles and other articles 
  of archaeological, ethnographical, historical or other scientific interest, 
  which are the property of the Commission or which by agreement with 
  the owner are under its control or for which it is trustee for the Government;</font></li>
</ol>

</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>fixing fees which shall be 
  payable to the Commission for such access;</font></li>
</ol>
</ul>
 <br>
<ul>
<ol type="a" start="3">
  <li>safeguarding national monuments, 
  ancient monuments, tablets, relics, ethnographical articles and other 
  articles of archaeological, ethnographical, historical or other scientific 
  interest from disfigurement, alteration, destruction or export; and</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="4">
  <li>regulating the excavation 
  of ancient monuments and ancient workings and the removal of relics 
  and ethnographical articles from ancient monuments or ancient workings.</font></li>
</ol>
</ul>
 <br>
<ul><p>(2) Such bye-laws may prescribe fines 
not exceeding ten pounds for contravention thereof or non-compliance 
therewith.</font> <br></p></ul>

<ul><p>(3) No such bye-law shall take effect 
until it has been approved by the Governor and published in the <i>Gazette</i>.</font> <br>
</p></ul>
<ol type="1" start="17">
  <li>(1) Any person who knowingly 
  –</font></li>
</ol>
 <br>
<ul>
<ol type="a">
  <li>fails to comply with or contravenes 
  any provision of this Ordinance;</font></li>

</ol>
</ul>
 <br>
<ul>
<ol type="a" start="2">
  <li>in any application to the 
  Commission for its consent in terms of section 13 makes any statement 
  which is false in any material respect or supplies therewith any drawing 
  or photograph which is false in any material respect;</font></li>
</ol>
</ul>
 <br>
<ul><p>shall be guilty of an offence and 
liable on summary conviction to a fine of one hundred pounds or, in 
default of payment, to imprisonment for six months.</font> <br>
</p></ul>

<ul><p>(2) If any person is convicted of 
an offence in terms of this Ordinance which has resulted in damage to, 
destruction or removal of any national monument, ancient monument or 
relic or any part thereof, or any tablet erected by the Commission, 
or in damage to or destruction of any ethnographical article or any 
part thereof, the Court may, in addition to any other penalty which 
it may impose, order him to pay such sum as the Court may determine 
for the purpose of repairing such damage or for the value of any article 
or thing removed or destroyed.</font> <br></p></ul>
<p>Passed in the Legislative Council this 
22nd day of May, in the year of Our Lord one thousand nine hundred and 
forty-six.</font></p>
