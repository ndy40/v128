Majesty by and with the advice and consent of the House of Representatives 
in this present Parliament assembled, and by the authority of the same, 
as follows:</font> <br></p>
<ol type="1">
  <li>Section 2 of the principal 
  Act is hereby amended by substituting for the definition “Minister” 
  as therein defined the following new definition –</font></li>
</ol>
 <br>
<ul><p>“Minister” means the Minister 
for the time being charged with responsibility for matters relating 
to Education;”.</font> <br></p></ul>
<ol type="1" start="2">
  <li>Subsection (l) of section 
  7 of the principal Act, is hereby amended by the addition at the end 
  thereof of the following new paragraph – </font></li>

</ol>
 <br>
<ul><p>“(<i>l</i>) acquire, maintain and 
administer the Sierra Leone Museum founded by the Sierra Leone Society 
and all things movable or immovable thereunto pertaining including the 
land and building situate at the Lucien Genet Gardens, Pademba Road 
Freetown.”</font> <br></p></ul>
<p>Passed in the House of Representatives 
this <i>16th</i> day of <i>January</i>, in the year of our Lord one 
thousand nine hundred and sixty-seven.</font></p>

	
