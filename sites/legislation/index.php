<p><b>No. 45, 1962</b></font> <br>
</p>
<p><b>An Act to Amend the Monuments and 
Relics Act</b></font> <br></p>

<p>An Act to Amend the Monuments and Relics 
Act</font> <br></p>
<p> [15th November, 1962.]</font> <br>
</p>
<p>BE IT ENACTED by the Queen’s Most Excellent 
Majesty by and with the advice and consent of the House of Representatives 
in this present Parliament assembled, and by the authority of the same, 
as follows:</font> <br></p>
<ol type="1">
  <li>This Act may be cited as the 
  Monuments and Relics (Amendment) Act, 1962.</font></li>
</ol>
 <br>

<ol type="1" start="2">
  <li>Section 2 of the Monuments 
  and Relics Act (hereinafter called the principal Act) is hereby amended 
  by substituting for the definition of “ethnographical article” therein 
  the following new definition:</font></li>
</ol>
 <br>
<ul><ul><p>“ “ethnographical article” 
means –</font> <br></p></ul></ul>
<ul>
<ol type="a">
  <li>any remains of early European 
  settlement of colonisation, or</font></li>
</ol>
</ul>

 <br>
<ul>
<ol type="a" start="2">
  <li>any work of art or craftwork, 
  etc. including any statue, modelled clay figure, figure cast or wrought 
  in metal, carving, housepost, door, ancestral figure, religious mask, 
  staff, drum, bowl, ornament, utensil, weapon, armour, regalia, manuscript 
  or document, if such work of art or craftwork is of indigenous origin 
  and –</font></li>
</ol>
</ul>
 <br>
<ul><ul>
<ol type="i">
  <li>was made or fashioned before 
  the year 1937; or</font></li>
</ol>

</ul></ul>
 <br>
<ul><ul>
<ol type="i" start="2">
  <li>is of historical, artistic 
  or scientific interest, and is or has been used at any time in the performance, 
  and for the purposes of, any traditional African ceremony:</font></li>
</ol>
</ul></ul>
 <br>
<ul><p>Provided that in the case of a dispute 
as to whether any particular article does or does not fall within the 
terms of this definition the decision of the Minister shall be final.”</font> <br>
</p></ul>
<ol type="1" start="3">

  <li>Section 9 of the principal 
  Act is hereby repealed and replaced by the following new section –</font></li>
</ol>
 <br>
<ul>
<ol type="1" start="9">
  <li>The Minister may from time 
  to time, on the recommendation of the Commission by notice published 
  in the <i>Gazette</i> proclaim any shaft, cutting, tunnel or stone which 
  was made for mining purposes to be ancient working.</font></li>
</ol>
</ul>
 <br>

<ol type="1" start="4">
  <li>Section 17 of the principal 
  Act is hereby amended –</font></li>
</ol>
 <br>
<ul>
<ol type="a">
  <li>by the substitution for the 
  words “fine of” in the ninth line thereof of the words “fine not 
  exceeding”, and</font></li>
</ol>
</ul>
 <br>
<ul>

<ol type="a" start="2">
  <li>the insertion immediately 
  after the words “imprisonment for” in the tenth line thereof of 
  the words “a period not exceeding.”</font></li>
</ol>
</ul>
 <br>
<ol><ol><ol><ol type="1" start="5">
        <li>The Proclamation made under 
        paragraph (b) of section 9 of the principal Act on the 7th day of March, 
        1949 (which refers to “Nomoli”) is hereby revoked.</font></li>
</ol></ol></ol></ol>
 <br> <br>
<p>Passed in the House of Representatives 
this <i>1st</i> day of <i>August</i>, in the year of our Lord one thousand 
nine hundred and sixty-two.</font> <br></p>

