            <p>The Monuments and Relics Commission is the main statutory body responsible for the protection of cultural heritage in Sierra Leone.</p>
            
            <p>
                In 1946 an ordinance was passed 'to provide for the preservation of Ancient, Historical, and Natural Monuments, Relics, and other 
                objects of Archaeological, Ethnographical, Historical or other Scientific Interest'. This ordinance set up the Monuments and Relics 
                Commission as a corporate body. The members, appointed by the government, receive no salaries, but only reasonable travelling expenses 
                and subsistence; they elect one of their number as chairman. All ethnographical items can be exported only by the express permission 
                of the Commission. By an amendment of 1962, 'ethnographical article' was taken to mean any item ‘made or fashioned before the year 
                1937’.
            </p>
            
            <p>
                In the early days, the Commission's source of revenue consisted of grants, donations, fees and subscriptions. In 1967 the Commission 
                was placed under the direction of the Minister of Education, who granted it an annual subvention to carry on its work.
            </p>
            
            <p>
                Under powers granted to it by the 1947 ordinance, the Commission identified and declared a series of national monuments and relics, 
                including Bunce Island, St. John's Maroon Church, the De Ruyter Stone of 1664, Old Fourah Bay College building and others.
            </p>
            
            <p>
                One of the most important changes that a 1967 amendment to the Monuments and Relics Ordinance (Act, since 1961) made was to give power 
                to the Monuments and Relics Commission to 'acquire, maintain and administer the Sierra Leone Museum founded by the Sierra Leone 
                Society and all things immovable thereunto pertaining, including the land and building situate at the Lucien Genet Gardens, Pademba 
                Road, Freetown'. With the reorganization of ministries in 1973, both 'Monuments and Relics' and 'Museums' were placed under the 
                Ministry of Tourism and Cultural Affairs.
