            <h2>MCF Easmon</h2>
            <div>
                <div id="tabs-1">
                    <p>
                        One of the most significant figures in the preservation and promotion of Sierra Leonean’s cultural heritage 
                        is Dr. M.C.F. Easmon (1890-1972). Easmon was well-known medical doctor who, after retiring from the medical 
                        profession, presided over the Monuments and Relics Commission for many years and is regarded as the founder 
                        of Sierra Leone’s National Museum.
                    </p>
                    
                    <p>
                        McCormack Charles Farrell Easmon was born in Accra in the Gold Coast (Ghana) where his father, a prominent 
                        Krio doctor, was working at the time. He was educated at the CMS Grammar School in Freetown and later at St. 
                        Paul’s Preparatory School in London. He attended Epsom College in Surrey and, after graduating in 1907, was 
                        awarded scholarships to study medicine at the Medical School of St. Mary’s Hospital in London. He qualified 
                        in medicine and surgery in 1912, and the following year passed the examination of the London School of 
                        Tropical Medicine.
                    </p>
                    
                    <p>
                        When Easmon returned to Sierra Leone in 1913, he was refused a post in the West African Medical Service because 
                        of his colour. There were then two categories of doctors – Europeans in the ‘colonial service’ and Africans in 
                        the ‘local service’. Easmon, forced into the separate and lower category despite his excellent qualifications, 
                        became Country Hospital Native Medical Officer. But he campaigned for years against racial discrimination in the 
                        medical service, earning the antipathy of the British authorities who called him the ‘Yellow Peril’. During World 
                        War I, Easmon enlisted in the army and served as a medical officer in the Cameroons. After the war, he returned 
                        to England for further study and, in 1925, was awarded a medical qualification in obstetrics and gynaecology.
                    </p>
                    
                    <p>
                        For the next twenty years, M.C.F. Easmon served in a variety of medical posts in the Protectorate, where he 
                        acquired a profound interest in the customs and traditions of the upcountry peoples. He published a series of 
                        scholarly articles on Madam Yoko, various paramount chiefs, the famous Massaquoi Crown, and Sierra Leone’s 
                        distinctive country cloth. He also became an accomplished amateur historian, publishing several well-researched 
                        articles on the early history of the Freetown colony.
                    </p>
                    
                    <p>
                        Easmon retired from the medical service in 1945, and had the satisfaction of seeing the British finally unify the 
                        two separate medical services the following year. But at the age of fifty-five, Easmon embarked on an entirely new 
                        career. He served part-time on various government boards and was at one point a director of the Bank of Sierra Leone, 
                        but his principal interest was the preservation of Sierra Leone’s historical and cultural heritage. In 1947, he became 
                        the first chairman of the Monuments and Relics Commission, and presided over the official declaration of many national 
                        historic sites. As a member of the Sierra Leone Society, he spearheaded the founding of the Sierra Leone Museum in 
                        1957 and became its first curator, working untiringly to acquire worthy exhibits for the museum and to set up 
                        attractive displays. He also worked hard to acquaint the public with Sierra Leone’s history and culture, hosting a 
                        popular radio programme called ‘Sierra Leone in Retrospect’. In 1954, Easmon was awarded the Order of the British Empire 
                        (O.B.E.).
                    </p>
                    
                    <p>
                        M.C.F. Easmon fell ill while on holiday in England in 1969 and never returned to Sierra Leone. He had one his battle 
                        against racial discrimination in the medical service, but his work to preserve Sierra Leone’s historical and cultural 
                        heritage had only just begun.
                    </p>
