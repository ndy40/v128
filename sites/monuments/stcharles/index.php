                    <h3>Notes</h3>

                    <p>
                        <em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and 
                                Relics Commission, n.d. (p.5):</strong></em>
                    </p>

                    <p>
                        These are situated at Regent Village. St Charles Church is the oldest stone church in Sierra Leone and the third oldest in 
                        Africa. It was built by Rev. W.A.B. Johnson a few years after Sierra Leone became a Crown Colony. Governor Charles M’Carthy 
                        who played such a notable part in the settlement of the freed slaves in the Colony villages was very interested and gave 
                        material help to Rev. W.A.B. Johnson, in the building of the church. The church was built in the old Kings Yard in Regent, 
                        there being one in each village. This was the place where, immediately on arrival, the Liberated Africans were set free and 
                        maintained by the Liberated African Department until they could be settled. There is also a small portion of the King’s Yard 
                        wall still standing at Regent.
                    </p>

                    <p>
                        <em><strong>Excerpt from A.J.G. Wyse et al. 2002. Vistas of the Heritage of Sierra Leone. Freetown, Sierra Leone: Fourah Bay 
                                College & Sierra Leone National Museum. (p.21-22):</strong></em>
                    </p>

                    <p>
                        As a result of the Parish Plan which, through the benevolent effort of Governor Charles MacCarthy, was inaugurated in 1817, 
                        the Church Missionary Society (CMS) agreed to provide personnel to administer the Liberated African villages that were 
                        founded for the settlement of emancipated slaves, and among other things, to cater for the spiritual and educational welfare 
                        of the emancipees. To attain these objectives, government agreed to finance the erection of schools and churches. In 1816 
                        (the very year the Parish Plan was born) the colonial government financed the building of a stone church (Saint Charles’ 
                        Church) in the Liberated African village of Regent, which had been established in 1812 as Hogbrook. The church was opened in 
                        August 1816. During the period of superintendence by the Reverend W.A.B. Johnson (1816-1823) this village became a profound 
                        success as a Christian community and a paradigm of evangelistic outreach in Sierra Leone.
                    </p>

                    <p>
                        St. Charles, named after the Canonized benefactor of the Liberated Africans, Charles MacCarthy, has the distinction of being 
                        the first stone church to be built in West Africa, and it was consequently likened to the premier church in Anglican Britain. 
                        With the help of some soldiers of the Royal African Corp, the Liberated Africans of Regent enthusiastically provided labour 
                        for the construction of the edifice under the supervision of a European builder.
                    </p>

                    <p>
                        Constructed with red stones, perched on top of a steep hill, this church was planned to accommodate about five hundred 
                        worshippers, but in about three years after its construction it proved too small for the rapidly expanding population of 
                        Regent. Indeed, the bustling growth of the population could properly be attributed to the dynamic influence of the “Apostle 
                        of Regent” (Revd. W.A.B. Johnson) whose pastoral guidance put Regent in the lead among other Liberated African villages in 
                        bringing in and retaining Liberated Africans within the Christian fold.
                    </p>

                    <p>
                        In recognition of this development, the Governor provided funds for the installation of a gallery, which accommodated two 
                        hundred more worshippers. This however soon proved to be inadequate and so there was a further extension of the building, 
                        which, it was claimed, could accommodate a little under a thousand people.
                    </p>

                    <p>
                        These accretions unfortunately proved inadequate since on a good day, worshippers had to endure the discomfort of standing 
                        outside because they could not get into the church.
                    </p>

                    <p>
                        This burgeoning congregation however dwindled in 1823 with the demise of the charismatic and almost hypnotic sway which the 
                        Revd. W.A.B. Johnson appeared to have held over the inhabitants of Regent. Consequently, there was a considerable drop in 
                        church attendance after Johnson’s death.
                    </p>

                    <p>
                        Furthermore the cessation of public works, which had attracted a large number of people to the village as well as the 
                        tendency for the Liberated Africans to migrate to the city in search of greener pastures contributed to this development. 
                        Nonetheless, this did not detract from the glory and historic importance of St. Charles’, which some members have fondly and 
                        proudly referred to as the “Canterbury of West Africa”. As a relic, it tells both the story of missionary enterprise in 
                        Sierra Leone and illustrates the church architecture of the 19th century.
                    </p>

                    <p>
                        <strong><em>Excerpt from G.H. Anderson (ed.) 1999. Biographical Dictionary of Christian Missions. Grand Rapids, Michigan: Wm. 
                                B. Eerdmans. (pp.337-338):</em></strong>
                    </p>

                    <p>
                        Johnson, William Augustine Bernard (1787-1823), German-born missionary of the Church Missionary Society (CMS) in Sierra 
                        Leone. Born in Hanover, Germany, Wilhelm Augustin Bernhard Janzen moved with his wife to England in 1812. Working as a 
                        laborer in London, he Anglicized his name to “Johnson”. He offered his services to the CMS and in 1816 was sent to Sierra 
                        Leone as a schoolmaster. In 1817 he was ordained “according to the rites of the Lutheran church” by other German missionaries 
                        of the CMS... Johnson worked for seven years at the village of Regent as evangelist, social organizer, and eventually 
                        official superintendant. While the governor of Sierra Leone provided facilities, Johnson made striking progress in turning 
                        the flood of uprooted individuals – men, women and children of diverse cultural traditions – into an orderly body, English 
                        speaking and church attending ... Sent home because of ill-health, he died at sea. Publication of his journals made Regent a 
                        Victorian byword for the success of Protestant missions.
                    </p>
