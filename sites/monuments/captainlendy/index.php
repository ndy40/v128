                    <p>
                        <em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and Relics Commission, n.d. (p.7):</strong></em>
                    </p>
                    
                    <p>
                        Waiima is situated in the Fiama Chiefdom. It was here at Waiima in 1893 that the British and the French Forces who were allies 
                        fired accidentally on each other while pursuing Samory’s sofa warriors. It was a dark, misty morning, when the British fought 
                        the French, each party mistaking the other for the sofa warriors.
                    </p>
                    
                    <p>
                        By the time the mistake was realised, Captain Lendy on the British side and Lieutenant Maritz on the French side had been 
                        killed. Some other soldiers also died together with these two officers. Both Lendy and Maritz were buried on the spot and 
                        afterwards their brother officers erected a memorial cross over them. In 1933 a new and larger memorial was erected 
                        incorporating the original cross. The new memorial was erected by the Sierra Leone Government in memory of the soldiers who 
                        perished that day.
                    </p>
                    
                    <p><em><strong>Excerpt from T.J. Alldridge, The Sherbro and Its Hinterland, London: Macmillan, 1901 (pp.273-274):</strong></em></p>
                    
                    <p>
                        Close by was a steep bare gradient of granite leading to a high hill; upon the summit of which, standing out in relief 
                        against the sky, were a number of armed war-boys who had been sent from the town of Waima, which was an hour’s march further 
                        on. As we approached them it seemed as if they were under some misapprehension as to who or what we were. They had evidently 
                        come out with the intention of fighting. The Governor, however, who was at the head of the column, went forward with the 
                        interpreter and reassured them of the peacefulness of the expedition. With this explanation they were satisfied and pleased, 
                        and subsequently conducted us to the town of Waima, which was situated in a valley with some large cotton trees around it.
                    </p>
                    
                    <p>The town consists of about fifty huts.</p>
                    
                    <p>
                        It was on the 16th of April that we arrived at Waima. This was about four months after the unfortunate collision between the 
                        French and the English had occurred. Many of the huts were riddled by bullets. It will be sufficient for the purpose of this 
                        narrative to mention the kindly care which the Governor bestowed upon the graves of French and English, officers and men 
                        alike, who fell in that much-to-be-regretted action. By his orders the burial ground was cleared and the graves fenced in 
                        before we took our departure from that melancholy town, with its sorrowful surroundings and its most painful associations.
                    </p>
                    
                    <h4>Inspection 2007</h4>
                    
                    <p>
                        The memorials are well maintained in a brushed enclosure a short distance from the present village of Waiima. A number of 
                        impressive cotton trees tower above the area, which is evidently the site of the old village of Waiima (see proximity of 
                        huts in Alldridge’s photograph). (PB)
                    </p>
