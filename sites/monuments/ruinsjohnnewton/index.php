                    <p>
                        The ruins of John Newton’s House and the Slave Barracoons on the Plantain Island – The Plantain Island it is said, was first 
                        occupied by one Captain John Plantain and took their name from him and not from the presence of groves of plantains of which 
                        there are non now and by local tradition never have been any in the past.
                    </p>
                    <p>
                        At the present day there is a large fishing village, the headquarters of the local Bonga Industry. The remains of the slave 
                        factory on the small peninsular forming the northern tip of the Island consists of:
                    </p>
                    <ul>
                        <li>
                            An L shape piece of the outer wall of the compound about 7 to 8 feet high with a coping on the top of which pieces of 
                            broken glass are embedded.
                        </li>
                        <li>
                            Another smaller portion of a wall by the side of which is built a depression now silted up which may be either the 
                            entrance to the underground barracoon described by Ross in 1926, or a well.
                        </li>
                        <li>The remains of the foundation of a building.</li>
                        <li>A well preserved cannon.</li>
                    </ul>
                    <p>
                        John Newton, the son of a Captain in the Mercantile Marine, was born in 1725 and died in December, 1807, within a month of 
                        the transfer of the Colony from the Chartered Company to the Crown.
                    </p>
                    <p>
                        He frequently went to sea with his father and became a wild and turbulent youth. In his old age from the pulpit of St. Mary 
                        Woolnoth, Lombard Street, London, he spoke of himself “that one of the most ignorant, the most miserable, and the most 
                        abandoned of slaves should be plucked from the forlorn state of exile on the coast of Africa and at length be appointed 
                        Minister of the Parish of the first magistrate of the first city in the world.”
                    </p>
                    <p>
                        Destined by his parents for a post in a merchant’s office in Jamaica, he was seized during a drunken orgy by a Naval Press 
                        Gang in 1743. His father instead of buying him out had him made midshipman. His bad behaviour soon caused him to be degraded 
                        to the forecastle and in 1744, he sailed as an ordinary seaman in H.M.S. <em>Harwich</em> for Madeira. There the Captain on 
                        account of his continued unruly behaviour gladly exchanged him for a sailor off a Guineaman bound for Sierra Leone and the 
                        West Coast of Africa.
                    </p>
                    <p>
                        Arriving at Sierra Leone and seeing the wealth to be made out of the slave trade, he obtained his discharge without pay and 
                        landed on the Banana Islands where he entered the services of a white slave trader who soon transferred to the Plantain 
                        Island.
                    </p>
                    <p>
                        Here he stayed about a year, during which time, in the absence of his master he was very badly treated by the master’s wife 
                        who half starved him, and generally treated him worse than a slave. However, he managed to smuggle some letters to his father 
                        in England. Meanwhile he was able to transfer to another white master on the Boom Kittam River and it was here he was rescued 
                        by a ship sent out by his father, and after a very bad voyage reached England in 1748. The perils and salvations of this last 
                        voyage played a large part in turning his thoughts to religion. But the urge of the sea was still in him and between 1748 and 
                        1754 when he gave up the sea, he made at least three voyages to West Africa as a slave trader, going on with its human cargo 
                        to the West Indies and the Southern States of America. During these voyages, he was noted for strict religious studies and 
                        observances.
                    </p>
                    <p>
                        After leaving the sea, he became a Tide Server at Liverpool, and continuing his religious studies eventually became curate of 
                        Olney, Bucks. Here he became a friend of the poet Cowper, and collaborated with him in the “Olney Hymns.” Newton will perhaps 
                        best be remembered by posterity as a hymn writer... [notably for    hymns such as “Amazing Grace” and “How Sweet the Name of 
                        Jesus Sounds”.]
                    </p>
                    <p>He was at Olney for sixteen years and then became Rector of one of Wren’s City Churches, St. Mary Woolnoth, Lombard Street.</p>
                    <p>On his death in 1807, he was buried in the Churchyard but his remains were subsequently removed and reinterred in the Olney Churchyard.</p>

