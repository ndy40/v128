                    <ul>
                        <li>First voyage to Sierra Leone about 1744, when he stayed on the Banana Islands, the Plantain Island and the Boom Kittam River.</li>
                        <li>Paid at least three more visits between 1748 and 1754.</li>
                        <li>
                            Captain Butt Thompson reports that when Granville Sharp, the first of the Abolitionists was looking for a place to which 
