                    <h4>Inspection 2007:</h4>
                    <p>
                        As a result of coastal erosion, the tip of the peninsular referred to in the Monuments & Relics Commission Annual Report for 
                        1949 is now completely separated from the mainland of Plantain Island, though it can be reached at low tide on foot. Local 
                        informants say that the peninsular was once heavily forested. The L-shaped piece of the outer wall mentioned in the Report is 
                        still standing, although the ground on which the arm extending in the direction of Plantain Island is being undercut by tidal 
                        currents and the wall is here collapsing at a fast rate. Dislodged sections of masonry similar to that of the wall can be seen 
                        in the vicinity at low tide. No trace of the smaller section of wall, foundations or cannon could be found. (PB)
                    </p>
