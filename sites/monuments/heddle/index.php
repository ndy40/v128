                    <h3>Notes</h3>

                    <p><em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and Relics Commission, n.d. (p.1):</strong></em></p>

                    <p>
                        This was the site of a house, which commanded a fine view over Freetown. It is found on the old Leicester Road and dates from 
                        1820. This was a prominent residence, which is now in ruins. It belonged to several notable citizens before passing finally 
                        in 1859 to the Hon. Charles Heddle, a leading businessman and pioneer in the groundnut trade. Until it was abandoned, it was 
                        in turn a private residence; the country residence of the Governor; a convalescent home; the home of some Commissary Judges 
                        (Mrs. Melville, who was the wife of one of the judges, wrote “ A Residence at Sierra Leone”). Heddle sold it to the Government 
                        and it was last used as the Residence of the Comptroller of Forestry, when the extensive gardens were maintained by the 
                        Forestry Department. Sometime between 1962 and 1966, Heddles Farm became part of the Botanical Gardens of Fourah Bay College.
                    </p>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1948 (pp.5-6):</strong></em></p>

                    <p>
                        The earliest reference to a dwelling here is that mentioned in Captain Butt Thompson’s “Sierra Leone In History and Tradition.” 
                        In the middle of the eighteenth century a new Company, The Company of Merchants of Great Britain, were granted a Charter for 
                        trade in West Africa but were unable to obtain Bunce Island where the Governors of previous Companies had made their 
                        headquarters. Butt Thompson writes:
                    </p>

                    <p>
                        “After litigation won by the new owners the agent of the Merchants, William Knight, made his headquarters on a hill behind 
                        Freetown, where he lived for fifteen years on the site of a house now owned by the Forestry Department.”
                    </p>

                    <p>
                        In 1926 when Butt Thompson wrote the only house owned by the Forestry Department on the hills outside Freetown was Heddle’s 
                        Farm, where for a time resided the Conservator of Forests, Mr. Lane Pool.
                    </p>

                    <p>
                        The Hon. C.W. Heddle has been described as being one of the greatest European merchants the town has ever known. He had 
                        wharves at the foot of the old Wharf steps (now often erroneously called the Portuguese Steps) and his main business house 
                        lay to the east of them, somewhere near where now stands the main building of Messrs. C.F.A.O. He has other memorials besides 
                        the residence on the hills, in Heddle Street and Lane in Freetown and Heddle Swamp and Heddle Road, at Bonthe. The site of 
                        the present District Commissioner’s house at Bonthe at the west end of Heddle Road was bought from him or his estate.
                    </p>

                    <p>
                        In 1878 the farm was purchased by the Government from Mr. Heddle for £330. The property is described as being situate at 
                        Smith’s Hill or the Governor’s farm and consisted of 60 acres. The Authoress of “A Residence at Sierra Leone” lived for the 
                        greater part of two tours here and in her book, written about 1849, described the site of the farm and the views of Freetown 
                        and the harbour.
                    </p>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1952 (p.3):</strong></em></p>

                    <p>Addenda to Notes on Heddle’s Farm in the 1948 Report:</p>

                    <p>
                        Through the kindness of Mr. C.H. Fyfe I have received the following facts relating to the successive owners of Heddle’s Farm 
                        extracted from Government Archives.
                    </p>

                    <p>“Heddle’s Farm”:</p>

                    <p>
                        Lots 78-83, 110-112 were originally allotted as on the plan of Nova Scotian Country Allotments. Lot 113, adjoining, was given 
                        to Abram Smith, hence presumably, the name “Smith’s Hill” given to the Hill.
                    </p>

                    <p>
                        Twenty-ninth September, 1812, all those lots (and also what is now Mount Aureol) were granted to Governor Maxwell. They were 
                        afterwards sold to Governor Macarthy (without Mount Aureol), and the estate was known as “Governor’s Farm”.
                    </p>

                    <p>Fourth June, 1825, after McCarthy’s death, it was sold by auction to Kenneth Macauley and called “Coffee Farm”.</p>

                    <p>
                        Tenth February, 1832, it was sold by Kenneth Macauley’s executor to William Smith, judge in the Court of Mixed Commission, 
                        and called “Smith’s Hill” again.
                    </p>

                    <p>Twenty-sixth October, 1838, it was sold to the Chief Justice, Robert Rankin, and called “Bellevue”.</p>

                    <p>
                        Ninth October, 1843, Rankin’s widow sold it to M[ichael] L. Melville, judge in the Court of Mixed Commission [husband of 
                        Elizabeth Melville, authoress of A Residence at Sierra Leone].
                    </p>

                    <p>
                        Melville sold off a part of it on eighteenth July, 1846 to Peter Wilson, one of the clerks in the Mixed Court, and then seems 
                        to have disposed of it to Thomas Stevens Carue, an English Merchant.
                    </p>

                    <p>Twenty-third April, 1859, it was bought by Charles Heddle.</p>

                    <p>Thirty-first December, 1878, Heddle sold it to Government.</p>

                    <p><em><strong>
                                Excerpt from E.H. Melville, A Residence at Sierra Leone, Described from a Journal Kept on the Spot, and from Letters 
                                Written to Friends at Home, ed. C. Norton. London: John Murray, 1849 (pp.37-44):
                            </strong></em></p>

                    <p>
                        We came up to our new and very solitary habitation about three weeks ago, and as yet I like it infinitely better than 
                        Freetown; it is so much cooler and more pleasant every way, despite the steepness and difficulty of ascent. The house is 
                        not so large as the one we had in town, but this is rendered of less consequence by the surrounding atmosphere being much 
                        purer than that in Freetown.
                    </p>

                    <p>
                        Birds of every colour are for ever flitting past, and though their notes want variety, they are far from being unmusical. 
                        The humming-birds, scarcely larger than humble-bees, with plumage of green, blue, and purple, haunt the graceful boughs of 
                        a tamarind tree close to my room windows, and flutter round the scented yellow blossoms of a wild acacia that grows near 
                        the house.
                    </p>

                    <p>&hellip;</p>

                    <p>
                        At a cantering sort of pace the bearers bore the palanquin along under the shade of some fine orange-trees, and set it down 
                        in the open ground piazza of a building, whose winding outside staircase, projecting eaves, and strange sloping roof, 
                        reminded me, on a large scale, of the imitation Swiss cottages you see at watering-places or in the suburbs of large towns 
                        in our own country.... On entering a small low parlour, with arched windows and door in substantial stone walls, it appeared 
                        that our dwelling rose phoenix-like from the ashes of another, literally standing within the ancient foundation of what had 
                        once been a structure nearly twice as large as the present.
                    </p>

                    <p>
                        &hellip;It was strange to see, from the front windows of our mountain residence, the widely scattered lights of Freetown so 
                        far down beneath; while the barking of dogs, the singing and laughing of the natives, and the beat of drums, were mellowed 
                        into comparative softness as they rose out of the valley below and blended with the floating notes of the evening bugle from 
                        the barrack hill. At the back of the house all was gloom and solitude.
                    </p>

                    <p>&hellip;</p>

                    <p>The cottage is built on a ridge so narrow, that the ground slopes at both ends with precipitous abruptness&hellip;</p>

                    <p>
                        &hellip;From the front windows Freetown looks as if marked out upon a map on a gigantic scale; and although there is 
                        sameness and formality in the long straight streets, crossing each other at equal distances, yet the irregularity of the 
                        different buildings, embowered as  they all are in trees – the ships constantly in the harbour – the Bullom shore with 
                        its shining sand beach and perpetual verdure – the broad blue sea stretching out till bounded by the horizon – form a 
                        relief to what might otherwise be considered tame and wanting in variety. Between us and part of Freetown, the barrack-hill, 
                        with its crowning range of lofty buildings and smooth esplanade, rises up in grassy simplicity. Upon the esplanade stands 
                        what was once a martello tower, originally one of the defences of the colony, and from which the locality of the barracks 
                        is generally designated “Tower Hill”.
                    </p>

                    <h4>Inspection 2007</h4>

                    <p>
                        There is now very little to be seen of the house apart from some stone footings, the house platform, and evidence of 
                        landscaping of the garden. (PB)
                    </p>
