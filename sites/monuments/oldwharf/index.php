                <p>
                    When Sir Charles MacCarthy took over the Governorship of the Colony, there were very few stone buildings in Freetown, public or 
                    private. He started erecting public buildings constructed with stones; for example, a magnificent commissariat building was put 
                    up at the Government Wharf. He had the whole wharf rebuilt with stone breakwaters. He also ordered that a flight of stone steps 
                    be constructed. This was completed in 1818.
                </p>

                <p>
                    These are the steps between the lower Commissariat (where the offices of the National Electoral Commission are now located) and 
                    the French Company building (the huge refurbished building which houses the Auditing Company, KPMG) leading down from Water 
                    Street (now Wallace Johnson Street) to the landing stage. The scenic view was so fetching that someone once commented that this 
                    wide handsome flight of steps resembled a stream of water cascading down the wharf.
                </p>

                <p>
                    They are often being referred to as the “Portuguese steps”, which is quite erroneous as they were built centuries after the 
                    Portuguese had left this part of Africa. The Guard House at the top of the steps was built in 1819. MacCarthy who lived nearby, 
                    was out daily, superintending the work.
                </p>
