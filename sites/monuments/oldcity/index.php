                <p>
                    These are three late 18th Century cannons and they probably came from some condemned slave ship. They were placed half buried in 
                    the ground to mark the boundaries of Old Freetown. They were buried within a few feet of their muzzles leaving these two or three 
                    feet protruding above the ground. They subsequently became silted up and overgrown with bush. In 1953, the Monuments and Relics 
                    Commission undertook the task of digging up the guns. They initially had problems locating the guns and the exact boundary lines. 
                    Eventually when they were declared National Monuments by Public Notice 77 in 1953, they were mounted in a vertical position on 
                    short stone plinths with explanatory inscriptions on brass plates, which were made by the Sierra Leone Government Railway 
                    Workshop...
                </p>

                <ol>
                    <li>
                        The first and oldest gun is mounted at the junction of Kissy, Ross and Blackhall Roads. This gun dates from about 1800 and was 
                        first used to mark the division for road cleaning purposes between Freetown and Granville Town. Thus it marked the eastern 
                        limit of the city in 1801. The brass plate faced Kissy Road. This is the “Kissy Gun” and this part of the city is called “Up 
                        Gun”... The ‘Up Gun’ cannon is the only one of the three that is still identifiable and visible...
                    </li>
                    <li>
                        The second gun is on the Leicester Road or Mountain Road. It is mounted by the first mile from Freetown, which is now little 
                        more than a path. The new road goes round by Fourah Bay College. The gun here is not visible.
                    </li>
                    <li>
                        The third gun is at the junction of Pademba Road and Mereweather Road, which is now called Jomo Kenyatta Road, in Freetown. 
                        Again, it is presently difficult to visually locate this third bit of the boundary landmarks of the Old City of Freetown.
                    </li>
