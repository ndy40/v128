                    <h3>Notes</h3>

                    <p><em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and Relics Commission, n.d. (p.2):</strong></em></p>

                    <p>
                        This is Freetown’s oldest monument. It lies buried 6 feet underground, just above the high water mark at the King Jimmy 
                        Market, near the Connaught Hospital. Mentioned by early travellers in ships journals, it was covered by silt and was only 
                        re-discovered in 1923 when a concrete drain-off was being built to direct the waters of Peter’s Brook, beside the then new 
                        market building. The spring at this point was well known to all mariners on the West Coast of Africa as the best watering 
                        place in the region. It was then known as “King Jimmy Water” and is now called “Peter’s Brook”.
                    </p>

                    <p>
                        In 1664, during the Anglo-Dutch War, the famous Dutch Admiral De Ruyter sailed to Sierra Leone in order to destroy British 
                        settlements. He sacked Bunce Island and Tasso Island. He watered at King Jimmy and triumphantly recorded his visit by having 
                        his name and that of his second in command inscribed on a rock beside the clear brook, Peter’s Brook. The inscription on the 
                        syenite stone reads as follows:
                    </p>

                    <p>“M.A. Ruyter. I. C. Mellon, Vice Admiralen, West Fries, Vant  A.D. 1664”</p>

                    <p>
                        After inspection in 1923, the stone was reburied as protection against vandalism and weathering. It has since then been 
                        uncovered every ten years (1948, 1958, 1968, 1978 and 1987) for inspection and re-buried waiting the time when finances will 
                        be available to erect a permanent building over it which will allow viewing as well as protection.
                    </p>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1949 (p.5):</strong></em></p>

                    <p>
                        <em>The De Ruyter Stone</em> – An account appears in Sierra Leone Studies Vol. XIX page 158. This is Freetown’s No. 1 
                        Antiquity yielding only in pride of place to Bunce Island Fort which was obviously already in existence earlier, since the 
                        object of Admiral De Ruyter in visiting Sierra Leone in 1664 was to attack and destroy the English Factories and Forts at 
                        Tassoh and Bunce Island.
                    </p>

                    <p>
                        Finch who visited Sierra Leone in 1607 describes a stone on the eastern bluff of the 4th cave, this being the watering place, 
                        on which were inscribed the names of Hawkins and Drake. This is probably another stone not yet traced in modern times. This 
                        was seen by Commodore Thompson in 1787 when he brought out the first settlers and again described by Burton in “Wanderings in 
                        West Africa” in 1862.
                    </p>

                    <p>
                        The De Ruyter Stone was re-discovered in 1923 when a large outfall drain was being constructed. It was cleared, photographed 
                        and then for its protection buried in about six feet of rubble.
                    </p>

                    <p>
                        When this Commission was formed the stone was again uncovered in 1947 and measurements and plans made for permanently 
                        rendering it secure and at the same time visible to passers by. But for financial reasons it was covered up again until 
                        such time as funds are available for its permanent exposure.
                    </p>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1958 (p.2-3):</strong></em></p>

                    <p>
                        THE RUYTER STONE – During February 1958 this stone was uncovered by the Department of Information and the opportunity was 
                        taken to make several visits in an attempt to decipher more names than had so far been recorded.
                    </p>

                    <p>Various times of the day were chosen so as to find the best light... Names and inscriptions recorded before this attempt were as follows:</p>

                    <ul>
                        <li>The main inscription which gives the stone its name: M. A. Ruiter, I. C. Meppel Vice Admiralen van Hollant en Westuries Land Anno 1664.</li>
                        <li>Other names: Wm. Storym, I. Andriessen 1664, W. K. Rubert, Aandrens Pewe 1730, F. Scoplet, O. Scivsok, Clas Moore, I. I. Haen 1664.</li>
                    </ul>

                    <p>
                        It was felt that some of these had been read incorrectly, viz: O. Schrisok 12/9 (i.e. 12th September), F. Scoplet, Wm. Storyn. 
                        This date 12/9 occurs also under the main inscription and under I. Andriesen and it may be inferred that De Ruiter landed at 
                        this spot on the 12th September, 1664.
                    </p>

                    <p>One other name was deciphered: P. H. Neeleman.</p>

                    <p>Portions of names were: A.K. Hil..son, . ovener. ...mew Jolveroh. e, Harl. st.nger, John..., And ..., Str. ..., Grot..., William ...</p>

                    <p>Initials were: AA, 16 EA 64, ED, FBH, GC, GO, 1729, GW 1730, HC 1729, W 1729.</p>

                    <p>Other dates on the stone included: 1721, 1723, 1728, 1781, 1793.</p>

                    <p>On the south East corner of the stone was a very clear inscription which so far has defied interpretation: ISSCH/P.</p>
