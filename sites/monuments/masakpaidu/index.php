                    <h3>Notes</h3>

                    <p><em><strong>Excerpt from D. Cummings, The National Monuments of Sierra Leone: A Brief Guide, Sierra Leone: Monuments and Relics Commission, n.d. (p.2-3):</strong></em></p>

                    <p>
                        Masakpaidu is an abandoned village in Kono in Nimmi Yema Chiefdom and was one of several villages in the region fortified in 
                        pre-protectorate days. This fortified village of Masakpaidu dates back to about 1800.
                    </p>

                    <p>
                        The village was at the point where the Rivers Bafi and Bagwa unite to form the Sewa River, and was situated on a flat piece of 
                        raised ground. 12-foot stakes of the Cotton Tree enclosed the fortified area. Some of these tree stakes took root and the 
                        stumps remain till today. Outside the row of stakes was a ditch about 300 yards long. The depth of the ditch is now 6 feet 
                        below ground level on the inner side, and the distance to the top ramp of the outer wall is 9 feet; the width is about 12 feet. 
                        The ditch must have filled up with time, and is believed to have been much deeper. There were originally two entrances to the 
                        stockade, with poles place across the entrance to form a bridge. These were taken up at night or when an attack was expected.
                    </p>

                    <p>The village was finally abandoned when the Sofas under Alimamy sacked it around 1893.</p>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1949 (p.6):</strong></em></p>

                    <p>
                        These old earthworks are to be found on the site of the deserted village of Masakpaidu in the Nimmi Yema (Kono) Chiefdom of 
                        the South-eastern District in the fork, where the rivers Bafi and Bagbwe unite to form the Sewa River, and are on a flat piece 
                        of raised ground. The area enclosed has a diameter of about 100 yards and was surrounded by 8 feet stakes cut from branches of 
                        the cotton trees. Some of these took root and the stumps remain. Outside the row of stakes is a ditch about 300 yards long. 
                        The depth of the ditch is now 6 feet below the ground level on the inner side, and to the top ramp of the outer wall is 9 feet. 
                        The width at the top is 12 feet. There were originally two entrances and poles, which were taken up at night or when an attack 
                        was expected, were placed across each to form a bridge.
                    </p>

                    <p>
                        The age of these fortifications can be gauged from the fact that some of the surrounding cotton trees have fallen and the 
                        rotting trunks still remain some having fallen outwards and others inwards. The village was built by Kundina and probably 
                        dates from 1800.
                    </p>

                    <p>
                        Masakpaidu was one of several Kono villages fortified to withstand attacks from the nearby Korankos and Mendis and Temnes. It 
                        was sacked by Alimamy Samory during the Sofa Wars about 1893 and was never reoccupied.
                    </p>

                    <h4>Inspection 2007</h4>

                    <p>
                        The site is adjacent to the modern village of Massabendu. The name ‘Masakpaidu’ does not seem to be known locally, but it is 
                        not clear whether Masakpaidu is an incorrect rendering of Massabendu or whether this was an earlier form of the name. A single 
                        cotton tree remains of the original stockade. Such sites, often marking the area of an abandoned ‘old town’ and adjacent to a 
                        new town of the same name, are not uncommon throughout Sierra Leone. (PB)
                    </p>
