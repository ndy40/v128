                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1955-6 (p.5):</strong></em></p>

                    <p>
                        This is one of the oldest places of worship in the City dating from 1820 when the founder received a Crown Grant of the land 
                        between Percival and Liverpool Streets with frontage in Westmoreland Street [now Siaka Stevens Street]. The small white 
                        building surrounded by a low, white wall standing in its own grounds forms a welcome break in the monotony of Westmoreland 
                        Street. It is the only Church that bears the name of one of the original groups who formed this Colony, the Maroons from 
                        Jamaica, and it is in the part of Freetown originally allotted to them and called Maroon Town.
                    </p>

                    <p>
                        There is now only a small band of worshippers and not of the most affluent in the community and in order to preserve the 
                        fabric and discourage or prevent this property passing into other hands and being demolished to make way for more utilitarian 
                        buildings this proclamation was made. It still continues to be used as a place of worship.
                    </p> 

                    <p>
                        <em><strong>Excerpt from A.J.G. Wyse et al. 2002. Vistas of the Heritage of Sierra Leone. Freetown, Sierra Leone: Fourah Bay 
                                College & Sierra Leone National Museum. (p.26):</strong></em>
                    </p>

                    <p>
                        The small white painted Church with quaint architecture standing at the middle point of Siaka Steven Street, is the effort of 
                        the Maroons, the black returnees from Jamaica, who arrived in Freetown in 1800.
                    </p>

                    <p>
                        Survivors and descendants of slaves whose original African home was the Gold Coast, they had lived for the greater part of 
                        their stay in Jamaica outside the control of the white owners of the plantations where they had been taken to work, and as 
                        such were able to establish a socio-cultural structure that was largely African, and a community that was governed by their 
                        own leaders.
                    </p>

                    <p>
                        They brought this with them when they came to Sierra Leone. There they met the earlier settlers, the Nova Scotians, who had 
                        already been acculturated. It was these who began to transform the wild and aggressive Maroons into a literate Christian 
                        community. It was not an easy task, as the relations between the genteel Nova Scotians and the intemperate and bellicose 
                        Maroons were not always harmonious. Nevertheless, a good many of the latter were christianised, and in a few years a small 
                        group of God fearing Maroons had emerged, led by Charles Shaw Harding.
                    </p>

                    <p>
                        It was this group that seceded from the Churches of Nova Scotians, allegedly repelled by these early Christians’ pretentious 
                        arrogance, to found the Maroon Church which was erected in 1822. Reportedly some of the most accomplished stone masons and 
                        metal workers ever to return to Black Africa, they stamped these credentials on the structure of their church, hence its 
                        unique architecture.
                    </p>

                    <p>
                        The liturgy of the group basically was Methodism, but the history of the Church is replete with repudiation of the authority 
                        of mainstream Methodism and a consistent determination to remain independent. This however, came to an end when it eventually 
                        merged with the Wesleyan Methodists, in 1900, to become a circuit in the Methodist establishment.
                    </p>

                    <p>
                        ...[The] Maroon community was always a small one, but the church was at the centre of the social and religious activities of 
                        the people of Maroon Town, from Walpole Street to Kingtom, which was the extent of their location. Although Maroons had fully 
                        integrated by the third decade of the 19th century with the other settlers to forge a new group, the Krio, and over time had 
                        dispersed throughout the country, nonetheless a good many of the original Maroon families continue to worship in their 
                        community church, which happens to be one of the oldest churches in Sierra Leone.
                    </p>
