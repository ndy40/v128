                    <h3>Notes</h3>

                    <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1949 (p.6-7):</strong></em></p>

                    <p>Gateway to The King’s Yard – The date is 1817 and the inscription reads:</p>

                    <p>“ROYAL ASYLUM AND HOSPITAL FOR THE AFRICANS RESCUED FROM SLAVERY BY BRITISH VALOUR AND PHILANTHROPY”

                    <p>
                        After the Abolition Act of 1807 and the transfer in 1808 of the Colony to the Crown, the ships of the Atlantic Fleet were 
                        largely employed in the suppression of the slave traffic to the West Indies and the Americas. The captured vessels were 
                        naturally brought to the nearest British Colony, Sierra Leone, and an Admiralty Court was set up about 1809 to deal with the 
                        legal aspects of the liberation of their human cargoes and the condemnation of the vessels. In 1819, this part of the Court’s 
                        duties was handed to an International Court popularly known as the Mixed Commission Court.
                    </p>

                    <p>
                        Slaves were landed at King Jimmy Wharf and taken to a compound erected for their reception on the nearby bluffs to the West 
                        of the landing place.
                    </p>

                    <p>
                        This became known as the King’s Yard, and through this passed the immediate ancestors of nearly all the present-day 
                        inhabitants of the Colony proper, excluding the prior Nova Scotians and Maroons and the later Protectorate people. These 
                        were called the Liberated Africans and most of the present Colony born are of Liberated African descent.
                    </p>

                    <p>
                        At each village, for the disposal of batches of rescued slaves, was formed a King’s Yard. The most important was that at 
                        Kissy and the present Asylum block is on the site of the original King’s Yard. With the abolition of the slave traffic and 
                        the closing down of the Mixed Commission Court about 1870, there was no longer any use for this yard and in 1880, it was 
                        converted to the Colonial Hospital which was burnt down in 1920. [The site was later converted into a private paying block 
                        of the Connaught Hospital.]
                    </p>

                    <p>[PB: There were originally two gateways into the King’s Yard]</p>				
