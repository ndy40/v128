                <h3>Notes</h3>

                <p><em><strong>Excerpt from ‘Monuments and Relics Commission Annual Report’, Sierra Leone: Freetown, 1950 (p.3-4):</strong></em></p>

                <p>
                    <em>Cleveland Tombstone</em> – The following is a brief Historical Note as handed down among the Caulker Chiefs at Shenge and 
                    was told to [M.C.F. Easmon] by the [then] present Paramount Chief A.T. Caulker and his Chiefdom Clerk, Mr. Dillett.
                </p>	

                <p>
                    About the middle of the 18th Century two Europeans arrived in the Kagboro Area: James Cleveland who finally set up at the Banana 
                    Islands and Skinner Caulker who stayed on the main land at Tassoh not far from the present Shenge Town in the Kagboro area. At 
                    that time there was already a slave dealer at the Plantain Islands. Whether it was the original Captain John Plantain or a 
                    successor I am not sure. Skinner Caulker did business with him.
                </p>	

                <p>
                    On the death of James Cleveland and Skinner Caulker their Mulatto sons fell out and warred against each other. As he could more 
                    easily obtain gun powder, Thomas Steven Caulker defeated the Clevelands, took over the Bananas and carried away the Tombstone of 
                    James Cleveland and placed it over his own father’s grave at Tassoh. This old town has long ago been abandoned and a sacred bush 
                    allowed to grow, and here are still buried all the Caulker Chiefs.
                </p>	

                <p>
                    The Tombstone in more recent years was taken from the bush and brought into Shenge Town but it soon had to be taken back to the 
                    sacred bush as the neighbours declared it was haunted.
                </p>	

                <p>
                    This Tombstone is made of the same stone as those on Bunce Island and is of the same period, the date being 1791. But it is in a 
                    much better state of preservation the only damage being a clean break across the upper third caused by a tree having fallen 
                    across it.
                </p>	

                <p>The stone is flat and placed horizontally on a low mound and bears the following inscription:</p>	

                <p>
                    Sacred to the Memory of Mr. James Cleveland late Proprietor of this Island who departed his life March 24, 1791 in the 37th year 
                    of his age. His surviving relative William Cleveland has caused this stone to be placed over this Grave as a tribute to the 
                    Memory of a Worthy Man.
                </p>	

                <p>[Either side of a skull and crossbones is marked ‘MOMENTO MORI’]</p>	

                <p><strong><em>Excerpt from K. Manson & J. Knight. 2009. Sierra Leone, Bradt Travel Guide. Chalfont St Peter, Bucks. (p.202):</em></strong></p>

                <p>
                    The Caulker dynasty, which still rules Shenge, is descended from Thomas Corker, a young Englishman who arrived at Sherbro from 
                    London in 1684 in the employ of the Royal African Company. He rose to the post of chief agent in 1692 and married a princess of 
                    the Ya Kumba family, which ruled the shores of Yawri Bay further north. The family was headquartered at Plantain Island.
                </p>

                <p>
                    In the 18th century, the marriage of a Caulker daughter with William Cleveland, an English slave trader from Banana Islands, 
                    seemed to consolidate the position of both houses, giving the Caulkers a foothold on the Peninsula. Their son James was an 
                    extraordinary mix, exposed to the shady secret workings both of the poro society and the no less devilish rules and protocols 
                    of English public school. ‘With a White Man he is a White Man, with a Black Man he is a Black Man,’ John Matthews, another former 
                    slave trader, commented in 1789.
                </p>

                <p>
                    Ambition made him ruthless. In 1785, still smarting from his self-created hatred of the Caulkers, who controlled the region, he 
                    sent an army to Plantain Island. They cut off the head of the local ruler, Chief Charles Caulker, and with that James stuck out 
                    his own neck as the most powerful ruler along the coast.
                </p>

                <p>
                    James was succeeded on his death in 1791 by his nephew William, who lacked his steel. When the Caulkers put aside their own 
                    internal rivalries and got around to retaliating in 1798 William fled on a slave ship, and the Banana Islands came back under 
                    new chief Stephen Caulker’s control. In an act of revenge, Caulker removed James Cleveland’s tombstone from the island and it 
                    ended up as the doorstep to his own family burial ground near Shenge.
                </p>

                <p><strong><em>Excerpt from J. Corry. 1807. Observations Upon the Windward Coast of Africa. London: G. and W. Nicol. (pp.8-9):</em></strong></p>

                <p>
                    To the southward of Cape Sierra Leone, and in about 8 degrees north latitude, lie the Islands of Bannana, in a direction from 
                    east to west. To the west of Great Bannana, lie the smaller islands, which are little more than barren rocks. The soil of the 
                    Bannanas is very fertile, and the climate healthy, from their proximity to the sea, and the refreshing breezes which  it bestows 
                    upon them. They take their name from a fruit so denominated; and are situated in the most eligible position for commerce, upon 
                    the Windward Coast; combining, from their fertility of soil and situation, great agricultural advantages, and peculiar salubrity 
                    of air. At present the sovereignty of these islands is contended for by two chiefs, of considerable intelligence and enterprise, 
                    named Caulker and Cleveland. Caulker appears to be the legitimate sovereign; Cleveland's forefathers having been established by 
                    Caulker's as trade men, on their account; and by intermarriage with that family their claims are founded. James Cleveland, who 
                    married king Caulker's sister, first began the war by his Grummettas, on the Bannanas, attacking Caulker's people on the 
                    Plantains. The result of this violence was, that Charles Caulker was killed in battle; and his body mangled and cut into pieces, 
                    in the most savage and cruel manner. In 1798, Stephen Caulker, the present chief, commenced war again, to revenge his brother's 
                    death; and the barbarous contest has continued ever since, marked with ferocious cruelty, and with various success to the 
                    respective claimants. Soon after its renewal, James Cleveland died, and was succeeded by his nephew, William, who has received 
                    his education in England, and is a chief of no inconsiderable acquirements and talent. Stephen Caulker has succeeded in obtaining 
                    from him the possession of the Bannanas and Plantains, and at present sways authority over them; still, however, exposed to the 
                    enterprising genius and intrigues of Cleveland.
                </p>
