                <p>The SierraLeoneHeritage.org digital resource is the main output of a research project entitled ‘Reanimating Cultural Heritage: 
                Digital Repatriation, Knowledge Networks and Civil Society Strengthening in Post-Conflict Sierra Leone’. The project was funded 
                between 2009 and 2012 by the UK’s <a href="http://www.ahrc.ac.uk">Arts and Humanities Research Council</a> as part of its 
                <a href="http://www.beyondtext.ac.uk/">Beyond Text</a> programme and was directed by 
                <a href="http://www.ucl.ac.uk/archaeology/people/staff/basu">Dr Paul Basu</a> of 
                <a href="http://www.ucl.ac.uk/">University College London</a>. The project’s Informatics team was led by 
                <a href="http://www.sussex.ac.uk/informatics/people/peoplelists/person/2898">Dr Martin White</a> of the 
                <a href="http://www.sussex.ac.uk/">University of Sussex</a>.</p>
                
                <p>The ‘Reanimating Cultural Heritage’ project was concerned with innovating digital curatorship in relation to Sierra Leonean 
                collections dispersed in the global museumscape. Building on research in anthropology, museum studies, informatics and beyond, 
                the project considered how objects that have become isolated from the oral and performative contexts that originally animated 
                them can be reanimated in digital space alongside associated images, video clips, sounds, texts and other media, and thereby be 
                given new life. At the project’s heart was a series of collaborations between museums including the 
                <a href="<?php echo $baseUrl; ?>/museum/">Sierra Leone National Museum</a>, 
                the <a href="http://www.britishmuseum.org/">British Museum</a>, 
                <a href="http://www.brighton-hove-rpml.org.uk/Museums/brightonmuseum/Pages/home.aspx">Brighton Museum and Art Gallery</a>, 
                <a href="http://www.glasgowlife.org.uk/museums/Pages/home.aspx">Glasgow Museums</a>, the 
                <a href="http://www.liverpoolmuseums.org.uk/wml/">World Museum Liverpool</a>, and the 
                <a href="http://sounds.bl.uk/">British Library Sound Archive </a>. The project also engaged in capacity building activities in the 
                cultural sector in Sierra Leone and commissioned the production of videos on cultural heritage themes from Sierra Leonean partner 
                organisations including <a href="http://www.ballantaacademyofmusic.org/">Ballanta Academy of Music</a>, 
                <a href="http://www.iearnsierraleone.org/">iEARN-Sierra Leone</a>, and 
                <a href="http://www.sfcg.org/programmes/sierra/sierra_talking.html">Talking Drum Studios</a>.</p>
                
                <p>Another key objective of the project was to experiment with the integration of 
                web-based social networking technologies into the digital heritage resource in order to (re)connect objects in museum collections 
                with disparate communities and to foster reciprocal knowledge exchange across boundaries. Visitors to SierraLeoneHeritage.org can 
                become part of its community, contribute comments, engage in discussions, and upload their own images and videos by joining its 
                <a href="http://www.facebook.com/pages/Sierra-Leone-Heritage/104060172993181">Facebook group</a>.</p>
                
                <p>The SierraLeoneHeritage.org digital resource was launched first at the Sierra Leone National Museum in November 2011, and subsequently 
                at a special event and exhibition at University College London in February 2012. Portable copies of the resource have been distributed 
                to many schools in Sierra Leone and are used in ongoing outreach activities by the Sierra Leone National Museum.</p>
                
                <p>Historically, cultural heritage has been a low priority in Sierra Leone. The hope is that by reanimating these dispersed collections 
                and the differently-situated knowledges that surround them, Sierra Leone’s rich cultural heritage can be better appreciated and 
                contribute to the reanimation of Sierra Leonean society more generally.</p>
                
